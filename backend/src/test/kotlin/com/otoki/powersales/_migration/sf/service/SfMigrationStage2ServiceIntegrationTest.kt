    @Test
    @Transactional
    @DisplayName("leader-profile-flags — 멱등 (2회 실행 시 두 번째는 0 row)")
    fun `leader profile flags is idempotent`() {
        seedLeaderProfile()

        assertThat(service.runLeaderProfileFlags().totalRowsAffected).isEqualTo(1)
        em.flush()
        em.clear()
        // H2/PG 모두 동일 값 UPDATE 는 row 를 다시 세지만, 값 자체가 SoT 와 일치해야 한다 (회귀 방지).
        service.runLeaderProfileFlags()
        assertThat(boolOf("SELECT permissions_api_enabled FROM powersales.profile_flags")).isTrue()
        assertThat(boolOf("SELECT is_locally_modified FROM powersales.profile_flags")).isFalse()
    }

    private fun labelRows(resp: com.otoki.powersales._migration.sf.dto.SfMigrationStage2Response, labelContains: String): Int =
        resp.results.first { it.label.contains(labelContains) }.rowsAffected

    private fun strOf(sql: String): String? = objOf(sql)?.toString()
    private fun longOf(sql: String): Long = (objOf(sql) as Number).toLong()
    private fun boolOf(sql: String): Boolean = objOf(sql) as Boolean
    private fun objOf(sql: String): Any? {
        val rows = em.createNativeQuery(sql).resultList
        return if (rows.isEmpty()) null else rows[0]
    }
