    @Test
    @Transactional
    @DisplayName("Stage 2-C password — sfid IS NOT NULL + password NULL 인 user 의 password 를 사번 기반 BCrypt 로 채움")
    fun runPasswordHash() {
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, sfid) VALUES ('E100', '005AAAAAA000001AAA')").executeUpdate()
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, sfid, password) VALUES ('E101', '005AAAAAA000002AAA', '')").executeUpdate()
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, sfid, password) VALUES ('E102', '005AAAAAA000003AAA', 'ALREADY-HASHED')").executeUpdate()
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code) VALUES ('E103')").executeUpdate()

        val response = service.runPasswordHash()

        assertThat(response.substep).isEqualTo("password")
        assertThat(response.totalRowsAffected).isEqualTo(2)

        val pw100 = strOf("SELECT password FROM powersales.\"user\" WHERE employee_code = 'E100'")
        val pw101 = strOf("SELECT password FROM powersales.\"user\" WHERE employee_code = 'E101'")
        assertThat(pw100).startsWith("\$2a\$")
        assertThat(pw101).startsWith("\$2a\$")
        // 평문은 사번 기반 "{사번}@pwrs" — row 마다 다르다.
        assertThat(BCryptPasswordEncoder().matches("E100@pwrs", pw100)).isTrue()
        assertThat(BCryptPasswordEncoder().matches("E101@pwrs", pw101)).isTrue()
        // 다른 사번의 평문이나 사번 자체로는 매칭되지 않는다.
        assertThat(BCryptPasswordEncoder().matches("E101@pwrs", pw100)).isFalse()
        assertThat(BCryptPasswordEncoder().matches("E100", pw100)).isFalse()
        assertThat(pw100).isNotEqualTo(pw101)

        // password_change_required 강제 변경 플래그 설정 확인.
        assertThat(boolOf("SELECT password_change_required FROM powersales.\"user\" WHERE employee_code = 'E100'")).isTrue()

        assertThat(strOf("SELECT password FROM powersales.\"user\" WHERE employee_code = 'E102'")).isEqualTo("ALREADY-HASHED")
        assertThat(objOf("SELECT password FROM powersales.\"user\" WHERE employee_code = 'E103'")).isNull()

        // 멱등 — 재실행 시 변경 0.
        val again = service.runPasswordHash()
        assertThat(again.totalRowsAffected).isEqualTo(0)
    }

    @Test
    @Transactional
    @DisplayName("user profile reconcile — profile_sfid(SF 권위)로 profile_id override + 시스템 관리자 격상 보존 + 멱등")
    fun runUserProfileSfidReconcile() {
        // profile 4종 (진단 시나리오 정합): 5.영업사원 / 6.조장 / 9. Staff / 시스템 관리자
        em.createNativeQuery("INSERT INTO powersales.profile (profile_id, sfid, name) VALUES (24, '00e2x000000u01bAAA', '5.영업사원')").executeUpdate()
        em.createNativeQuery("INSERT INTO powersales.profile (profile_id, sfid, name) VALUES (25, '00e2x000000u01cAAA', '6.조장')").executeUpdate()
        em.createNativeQuery("INSERT INTO powersales.profile (profile_id, sfid, name) VALUES (9, '00e9x000000STAFAAA', '9. Staff')").executeUpdate()
        em.createNativeQuery("INSERT INTO powersales.profile (profile_id, sfid, name) VALUES (1, '00e0x000000ADMIAAA', '시스템 관리자')").executeUpdate()

        // U1: SF=6.조장(sfid ...u01c) 인데 provisioning fallback 으로 5.영업사원(24) 선점 → 6.조장(25)로 교정 대상
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('U1', '00e2x000000u01cAAA', 24)").executeUpdate()
        // U2: SF=6.조장 인데 9. Staff(9) 선점 → 6.조장(25)로 교정 대상
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('U2', '00e2x000000u01cAAA', 9)").executeUpdate()
        // U3: 이미 SF정답(6.조장, 25)과 일치 → 멱등 (미대상)
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('U3', '00e2x000000u01cAAA', 25)").executeUpdate()
        // U4: 현재 profile 이 '시스템 관리자'(1) 격상 계정 (SF정답은 9. Staff) → 보존 (되돌리지 않음)
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('U4', '00e9x000000STAFAAA', 1)").executeUpdate()
        // U5: profile_sfid NULL (SF 마이그레이션 아님 — 신규 provisioning) → 미대상 (그대로 유지)
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('U5', NULL, 24)").executeUpdate()
        // U6: profile_sfid 는 있으나 profile 테이블에 매칭 row 없음 → 미대상 (조인 실패, 그대로 유지)
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('U6', '00eXXXXXXNOMATCAA', 24)").executeUpdate()

        val response = service.runUserProfileSfidReconcile()

        assertThat(response.substep).isEqualTo("userProfileSfidReconcile")
        // 교정 대상은 U1 + U2 만 (2건)
        assertThat(response.totalRowsAffected).isEqualTo(2)

        // U1, U2 → 6.조장(25)로 교정
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = 'U1'")).isEqualTo(25L)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = 'U2'")).isEqualTo(25L)
        // U3 멱등 (이미 25)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = 'U3'")).isEqualTo(25L)
        // U4 시스템 관리자 격상 보존 (SF 9.Staff 로 되돌리지 않음 → 1 유지)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = 'U4'")).isEqualTo(1L)
        // U5 profile_sfid NULL → 미대상 (24 유지)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = 'U5'")).isEqualTo(24L)
        // U6 profile 매칭 없음 → 미대상 (24 유지)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = 'U6'")).isEqualTo(24L)

        // 멱등 — 재실행 시 변경 0
        val again = service.runUserProfileSfidReconcile()
        assertThat(again.totalRowsAffected).isEqualTo(0)
    }

    @Test
    @Transactional
    @DisplayName("user profile reconcile — SystemAdminGrantList 지정 사번은 SF(9.Staff) override 대신 시스템 관리자로 강제 격상 + 멱등")
    fun runUserProfileSfidReconcile_systemAdminGrantList() {
        // profile 2종: 9. Staff (SF 정답) / 시스템 관리자 (지정 격상 목표)
        em.createNativeQuery("INSERT INTO powersales.profile (profile_id, sfid, name) VALUES (9, '00e9x000000STAFAAA', '9. Staff')").executeUpdate()
        em.createNativeQuery("INSERT INTO powersales.profile (profile_id, sfid, name) VALUES (1, '00e0x000000ADMIAAA', '시스템 관리자')").executeUpdate()

        // 지정 사번 (SystemAdminGrantList 실제 사번 2개 샘플) — SF profile_sfid 는 9. Staff.
        // reset 직후 시나리오: 현재 profile_id 를 SF 정답(9)으로 선점해 둔 상태 (아직 관리자 아님).
        val grantCode1 = "20000531" // 강현배
        val grantCode2 = "20210359" // 하헌준
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('$grantCode1', '00e9x000000STAFAAA', 9)").executeUpdate()
        // 이미 관리자(1)로 격상돼 있는 지정 사번 → 멱등 (변화 없음)
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('$grantCode2', '00e9x000000STAFAAA', 1)").executeUpdate()
        // 비지정 사번 (SF 9. Staff, 현재도 9) → SF override 대상이나 이미 일치라 무변화
        em.createNativeQuery("INSERT INTO powersales.\"user\" (employee_code, profile_sfid, profile_id) VALUES ('OTHER', '00e9x000000STAFAAA', 9)").executeUpdate()

        val response = service.runUserProfileSfidReconcile()

        assertThat(response.substep).isEqualTo("userProfileSfidReconcile")
        // grantCode1 만 9 → 1 (시스템 관리자) 로 격상. grantCode2 는 이미 1 (멱등). OTHER 는 override 대상이나 이미 9 라 무변화.
        assertThat(response.totalRowsAffected).isEqualTo(1)

        // 지정 사번 grantCode1 → 시스템 관리자(1) 로 강제 격상 (SF 9.Staff override 아님)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = '$grantCode1'")).isEqualTo(1L)
        // 지정 사번 grantCode2 → 이미 관리자 유지 (1)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = '$grantCode2'")).isEqualTo(1L)
        // 비지정 OTHER → SF 정답 9 유지 (격상 안 됨)
        assertThat(longOf("SELECT profile_id FROM powersales.\"user\" WHERE employee_code = 'OTHER'")).isEqualTo(9L)

        // 멱등 — 재실행 시 변경 0
        val again = service.runUserProfileSfidReconcile()
        assertThat(again.totalRowsAffected).isEqualTo(0)
    }

