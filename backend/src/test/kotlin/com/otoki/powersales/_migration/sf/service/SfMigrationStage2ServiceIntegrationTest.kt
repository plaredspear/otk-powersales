    /** '6.조장' profile + profile_flags row 를 준비한다. @param profileId 를 flags 에 연결(null 이면 미연결). */
    private fun seedLeaderProfile(
        profileId: Long = 10L,
        linkFlagsToProfile: Boolean = true,
        isLocallyModified: Boolean = false,
    ) {
        em.createNativeQuery(
            "INSERT INTO powersales.profile (profile_id, sfid, name) VALUES ($profileId, '00e000000000001', '6.조장')"
        ).executeUpdate()
        val fkValue = if (linkFlagsToProfile) "$profileId" else "NULL"
        em.createNativeQuery(
            """
            INSERT INTO powersales.profile_flags
                (profile_id, profile_name, permissions_api_enabled, is_locally_modified)
            VALUES ($fkValue, '6.조장', FALSE, $isLocallyModified)
            """.trimIndent()
        ).executeUpdate()
        em.flush()
        em.clear()
    }

    @Test
    @Transactional
    @DisplayName("leader-profile-flags — SoT 값으로 6.조장 flags 갱신 (api_enabled=true, object/custom 권한 적재)")
    fun `leader profile flags applies seed`() {
        seedLeaderProfile()

        val resp = service.runLeaderProfileFlags()

        assertThat(resp.substep).isEqualTo("leaderProfileFlags")
        assertThat(resp.totalRowsAffected).isEqualTo(1)
        assertThat(boolOf("SELECT permissions_api_enabled FROM powersales.profile_flags")).isTrue()
        // SoT 의 system 비트 — api_enabled 만 true, 나머지 false.
        assertThat(boolOf("SELECT permissions_view_all_data FROM powersales.profile_flags")).isFalse()
        assertThat(boolOf("SELECT permissions_modify_all_data FROM powersales.profile_flags")).isFalse()
        assertThat(strOf("SELECT object_permissions FROM powersales.profile_flags")).contains("Account")
    }

    @Test
    @Transactional
    @DisplayName("leader-profile-flags — 7.영업사원 + 조장 은 적용 대상 제외 (6.조장 단건 정책)")
    fun `leader profile flags targets only 6josang`() {
        seedLeaderProfile()
        // '7.영업사원 + 조장' 도 함께 존재시켜 대상에서 빠지는지 확인.
        em.createNativeQuery(
            "INSERT INTO powersales.profile (profile_id, sfid, name) VALUES (20, '00e000000000002', '7.영업사원 + 조장')"
        ).executeUpdate()
        em.createNativeQuery(
            """
            INSERT INTO powersales.profile_flags
                (profile_id, profile_name, permissions_api_enabled, is_locally_modified)
            VALUES (20, '7.영업사원 + 조장', FALSE, FALSE)
            """.trimIndent()
        ).executeUpdate()
        em.flush()
        em.clear()

        val resp = service.runLeaderProfileFlags()

        // 결과 라벨은 6.조장 1건만 — 7.영업사원 + 조장 은 아예 순회 대상에서 제외.
        assertThat(resp.results).hasSize(1)
        assertThat(resp.results[0].label).contains("6.조장")
        assertThat(
            boolOf("SELECT permissions_api_enabled FROM powersales.profile_flags WHERE profile_id = 20")
