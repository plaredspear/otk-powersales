            "INSERT INTO powersales.upload_file (object_type, parent_id, record_sfid) VALUES ('DKRetail__Proposal__c', NULL, 'a03SU000000001AAA')"
        ).executeUpdate()
        em.createNativeQuery(
            "INSERT INTO powersales.upload_file (object_type, parent_id, record_sfid) VALUES ('DKRetail__SiteAcitivity__c', NULL, 'a0D5A000000001AAA')"
        ).executeUpdate()
        // 이미 parent_id 가 채워진 row — 멱등성 검증용 (parent_type 은 파생 대상이나 parent_id 유지).
        em.createNativeQuery(
            "INSERT INTO powersales.upload_file (object_type, parent_id, record_sfid) VALUES ('DKRetail__Claim__c', 999, 'a01CL000000001AAA')"
        ).executeUpdate()
        // record_sfid 가 어느 부모와도 매칭 안 되는 dangling row.
        em.createNativeQuery(
            "INSERT INTO powersales.upload_file (object_type, parent_id, record_sfid) VALUES ('DKRetail__Claim__c', NULL, 'a01XXXXXXXXXXXAAA')"
        ).executeUpdate()
        // object_type 이 빈 모바일 등록 경로 row (claim) — object_type 무관하게 record_sfid 로 연결되어야 함.
        em.createNativeQuery(
            "INSERT INTO powersales.upload_file (object_type, parent_id, record_sfid) VALUES (NULL, NULL, 'a01CL000000001AAA')"
        ).executeUpdate()

        val response = service.runUploadFilePolymorphicParent()

        assertThat(response.substep).isEqualTo("uploadFilePolymorphicParent")

        // record_sfid 직접 조인 — object_type 무관하게 부모 sfid 매칭으로 parent_type + parent_id 동시 설정.
        // claim 'a01CL...'(id=100) record_sfid 인 미연결 row 2건 (object_type='DKRetail__Claim__c' 1건 + NULL 1건).
        assertThat(longOf("SELECT COUNT(*) FROM powersales.upload_file WHERE record_sfid = 'a01CL000000001AAA' AND parent_type = 'Claim' AND parent_id = 100"))
            .isEqualTo(2L)
        // object_type 이 NULL 이어도 record_sfid 로 Claim 연결됨 (핵심 — 모바일 등록 경로 복원).
        assertThat(longOf("SELECT parent_id FROM powersales.upload_file WHERE object_type IS NULL"))
            .isEqualTo(100L)
        assertThat(longOf("SELECT COUNT(*) FROM powersales.upload_file WHERE object_type IS NULL AND parent_type = 'Claim'"))
            .isEqualTo(1L)

        // notice / suggestion / site_activity 도 record_sfid 조인으로 연결.
        assertThat(longOf("SELECT parent_id FROM powersales.upload_file WHERE record_sfid = 'a02NO000000001AAA' AND parent_type = 'Notice'"))
            .isEqualTo(200L)
        assertThat(longOf("SELECT parent_id FROM powersales.upload_file WHERE record_sfid = 'a03SU000000001AAA' AND parent_type = 'Suggestion'"))
            .isEqualTo(300L)
        assertThat(longOf("SELECT parent_id FROM powersales.upload_file WHERE record_sfid = 'a0D5A000000001AAA' AND parent_type = 'SiteActivity'"))
            .isEqualTo(400L)

        // 이미 parent_id=999 로 연결된 row 는 멱등 (parent_id IS NULL 조건이라 안 건드림).
        assertThat(longOf("SELECT parent_id FROM powersales.upload_file WHERE record_sfid = 'a01CL000000001AAA' AND parent_id = 999"))
            .isEqualTo(999L)
        // record_sfid 가 어느 부모와도 매칭 안 되는 dangling row → parent_id NULL 유지.
        assertThat(objOf("SELECT parent_id FROM powersales.upload_file WHERE record_sfid = 'a01XXXXXXXXXXXAAA'"))
            .isNull()
    }

    @Test
    @Transactional
    @DisplayName("notice rta placeholder — dry-run 은 본문 미변경 + 집계만, apply 는 placeholder 치환 + 멱등")
    fun runNoticeRtaPlaceholderRewrite() {
        em.createNativeQuery("DROP TABLE IF EXISTS powersales.notice").executeUpdate()
        em.createNativeQuery(
            """
            CREATE TABLE powersales.notice (
                notice_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                contents VARCHAR(4000),
                is_deleted BOOLEAN
            )
            """.trimIndent()
        ).executeUpdate()

        // (1) 본문에 rtaImage <img> 2개 (refid 있음 + alt 에 &amp; 포함) — 치환 대상.
        val rtaTag1 =
            """<img src="https://x.file.force.com/servlet/rtaImage?eid=a02&amp;feoid=00N&amp;refid=0EM001" alt="A&amp;B"/>"""
        val rtaTag2 =
            """<img src="https://x.file.force.com/servlet/rtaImage?eid=a02&amp;refid=0EM002">"""
        em.createNativeQuery("INSERT INTO powersales.notice (notice_id, contents, is_deleted) VALUES (10, :c, FALSE)")
            .setParameter("c", "<p>본문</p>$rtaTag1 중간 $rtaTag2 끝")
            .executeUpdate()

        // (2) rtaImage 지만 refid 없음 — 치환 불가 (skip 집계).
        em.createNativeQuery("INSERT INTO powersales.notice (notice_id, contents, is_deleted) VALUES (20, :c, FALSE)")
            .setParameter("c", """<img src="https://x.file.force.com/servlet/rtaImage?eid=a02">""")
            .executeUpdate()

        // (3) 이미 placeholder 치환된 본문 — 멱등 (변경 없음). LIKE '%rtaImage%' 에 안 걸려 조회조차 안 됨.
        em.createNativeQuery("INSERT INTO powersales.notice (notice_id, contents, is_deleted) VALUES (30, :c, FALSE)")
            .setParameter("c", """<img src="notice-image://0EM099" data-refid="0EM099">""")
            .executeUpdate()

        // (4) is_deleted=TRUE 인 공지 — 대상 제외.
        em.createNativeQuery("INSERT INTO powersales.notice (notice_id, contents, is_deleted) VALUES (40, :c, TRUE)")
            .setParameter("c", """<img src="https://x.file.force.com/servlet/rtaImage?refid=0EM040">""")
            .executeUpdate()

        // --- dry-run: 본문 미변경 + 집계 반환 ---
        val dry = service.runNoticeRtaPlaceholderRewrite(dryRun = true)
        assertThat(dry.substep).isEqualTo("noticeRtaPlaceholder (dry-run)")
        assertThat(dry.totalRowsAffected).isEqualTo(2) // 치환 이미지 수 (notice 10 의 2개)
        assertThat(labelRows(dry, "변경 대상 공지 수")).isEqualTo(1)
        assertThat(labelRows(dry, "refid 없는 rtaImage 태그")).isEqualTo(1)
        // dry-run 이라 본문은 원본 그대로 (rtaImage 잔존).
        assertThat(strOf("SELECT contents FROM powersales.notice WHERE notice_id = 10")).contains("rtaImage")

        // --- apply: 실제 placeholder 치환 ---
        val applied = service.runNoticeRtaPlaceholderRewrite(dryRun = false)
        assertThat(applied.substep).isEqualTo("noticeRtaPlaceholder")
        assertThat(applied.totalRowsAffected).isEqualTo(2)

        val c10 = strOf("SELECT contents FROM powersales.notice WHERE notice_id = 10")!!
        // 치환 결과 = placeholder 형태 (kts 와 동일). data-refid + notice-image:// + alt(&amp; 재이스케이프).
        assertThat(c10).contains("""<img src="notice-image://0EM001" data-refid="0EM001" alt="A&amp;B">""")
        assertThat(c10).contains("""<img src="notice-image://0EM002" data-refid="0EM002">""")
        assertThat(c10).doesNotContain("rtaImage")
        // refid 없는 notice 20 은 미변경.
        assertThat(strOf("SELECT contents FROM powersales.notice WHERE notice_id = 20")).contains("rtaImage")
        // is_deleted notice 40 은 미변경.
        assertThat(strOf("SELECT contents FROM powersales.notice WHERE notice_id = 40")).contains("rtaImage")

        // --- 멱등: 재실행 시 변경 0 (이미 치환된 본문은 정규식 미매칭으로 skip) ---
        val again = service.runNoticeRtaPlaceholderRewrite(dryRun = false)
        assertThat(again.totalRowsAffected).isEqualTo(0)
        assertThat(labelRows(again, "변경 대상 공지 수")).isEqualTo(0)
    }

